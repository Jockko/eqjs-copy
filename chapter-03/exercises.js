//Write a function min that takes two arguments and returns their minimum.
function min(num1, num2) {
//if the first number is less than the second return that number
if(num1 < num2){
return num1
}
else if(num2 < num1){
  return num2;
}
else if(num1 === num2){
  return 0;
}
}

